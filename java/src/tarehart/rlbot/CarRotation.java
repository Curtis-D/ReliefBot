package tarehart.rlbot;

public class CarRotation {

    public float noseX;
    public float noseY;
    public float noseZ;
    public float roofZ;
    public float rollRightDesire;

    public CarRotation(float noseX, float noseY, float noseZ, float roofZ, float rollRightDesire) {
        this.noseX = noseX;
        this.noseY = noseY;
        this.noseZ = noseZ;
        this.roofZ = roofZ;
        this.rollRightDesire = rollRightDesire;
    }
}
