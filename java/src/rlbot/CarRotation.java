package rlbot;

public class CarRotation {

    public double noseX;
    public double noseY;
    public double noseZ;
    public double roofY;

    public CarRotation(double noseX, double noseY, double noseZ, double roofY) {
        this.noseX = noseX;
        this.noseY = noseY;
        this.noseZ = noseZ;
        this.roofY = roofY;
    }
}
